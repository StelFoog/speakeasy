import styles from '../styles/Home.module.css';
import Head from 'next/head';
import { ErrorText, LoginForm, SubmitButtonView } from '../components/HomeViews';
import { useState } from 'react';
import { useRouter } from 'next/router';
import { useDispatch } from 'react-redux';
import { setUser } from '../redux/user';

export default function Index() {
	const [password, setPassword] = useState('');
	const [pnr, setPnr] = useState('');
	const [error, setError] = useState('');

	const router = useRouter();
	const dispatch = useDispatch();
	return (
		<div className={styles.container}>
			<Head>
				<title>SpeakEasy</title>
				<meta name="description" content="Generated by create next app" />
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<main className={styles.main}>
				<h1 className={styles.title}>Welcome to SpeakEasy</h1>
				<div className={styles.description}>
					<LoginForm
						password={password}
						pnr={pnr}
						onPnrChange={setPnr}
						onPasswordChange={setPassword}
					></LoginForm>
					<SubmitButtonView
						action={'Login'}
						onSubmit={() => {
							fetch('/api/staff/login', {
								method: 'GET',
								headers: {
									'Content-Type': 'application/json',
									Authorization: `${pnr}+${password}`,
								},
							})
								.then((res) => res.json())
								.then((data) => {
									const { error, type, name } = data;
									if (!error) {
										dispatch(setUser({ name, pnr, password, type }));
										router.push('dashboard');
									} else {
										setError(error);
										setTimeout(() => {
											setError('');
										}, 5000);
									}
								})
								.catch((err) => console.log(err.statusText));
						}}
						isDisabled={!pnr || !password || pnr.length !== 10}
					></SubmitButtonView>
					<ErrorText isDisabled={!error} error={error} />
				</div>
			</main>
		</div>
	);
}
